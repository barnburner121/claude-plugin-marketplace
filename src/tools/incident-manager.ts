import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import { checkRateLimit, rateLimitError } from "../auth.js";

export function registerIncidentManagerTools(server: McpServer) {
  server.tool("incident_generate_workflow", "Generate incident management workflow", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "incident_generate_workflow", directory, instructions: "Generate incident management workflow in the specified directory." }, null, 2) }] };
  });
  server.tool("incident_create_templates", "Create incident response templates", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "incident_create_templates", directory, instructions: "Create incident response templates in the specified directory." }, null, 2) }] };
  });
  server.tool("incident_add_automation", "Generate incident automation (Pro feature)", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    if (tier === "free") { return { content: [{ type: "text" as const, text: "This is a Pro feature. Upgrade at https://mcpize.com/barnburner121/plugin-hub" }] }; }
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "incident_add_automation", directory, instructions: "Generate incident automation in the specified directory." }, null, 2) }] };
  });
}
