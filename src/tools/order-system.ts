import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import { checkRateLimit, rateLimitError } from "../auth.js";

export function registerOrderSystemTools(server: McpServer) {
  server.tool("order_generate_model", "Generate order management system", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "order_generate_model", directory, instructions: "Generate order management system in the specified directory." }, null, 2) }] };
  });
  server.tool("order_add_workflow", "Add order status workflow", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "order_add_workflow", directory, instructions: "Add order status workflow in the specified directory." }, null, 2) }] };
  });
  server.tool("order_add_fulfillment", "Generate fulfillment integration (Pro feature)", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    if (tier === "free") { return { content: [{ type: "text" as const, text: "This is a Pro feature. Upgrade at https://mcpize.com/barnburner121/plugin-hub" }] }; }
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "order_add_fulfillment", directory, instructions: "Generate fulfillment integration in the specified directory." }, null, 2) }] };
  });
}
