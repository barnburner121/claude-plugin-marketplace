import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import { checkRateLimit, rateLimitError } from "../auth.js";

export function registerCommentSystemTools(server: McpServer) {
  server.tool("comment_generate_system", "Generate comment/discussion system", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "comment_generate_system", directory, instructions: "Generate comment/discussion system in the specified directory." }, null, 2) }] };
  });
  server.tool("comment_add_moderation", "Add comment moderation", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "comment_add_moderation", directory, instructions: "Add comment moderation in the specified directory." }, null, 2) }] };
  });
  server.tool("comment_add_threading", "Generate threaded comments (Pro feature)", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    if (tier === "free") { return { content: [{ type: "text" as const, text: "This is a Pro feature. Upgrade at https://mcpize.com/barnburner121/plugin-hub" }] }; }
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "comment_add_threading", directory, instructions: "Generate threaded comments in the specified directory." }, null, 2) }] };
  });
}
