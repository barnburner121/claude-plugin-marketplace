import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import { checkRateLimit, rateLimitError } from "../auth.js";

export function registerLiveUpdatesTools(server: McpServer) {
  server.tool("live_setup_system", "Generate live update system", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "live_setup_system", directory, instructions: "Generate live update system in the specified directory." }, null, 2) }] };
  });
  server.tool("live_add_optimistic", "Add optimistic updates", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "live_add_optimistic", directory, instructions: "Add optimistic updates in the specified directory." }, null, 2) }] };
  });
  server.tool("live_add_conflict", "Generate conflict resolution strategy (Pro feature)", { directory: z.string().describe("Project directory"), api_key: z.string().optional() }, async ({ directory, api_key }) => {
    const { allowed, tier } = checkRateLimit(api_key);
    if (!allowed) return rateLimitError(tier);
    if (tier === "free") { return { content: [{ type: "text" as const, text: "This is a Pro feature. Upgrade at https://mcpize.com/barnburner121/plugin-hub" }] }; }
    return { content: [{ type: "text" as const, text: JSON.stringify({ action: "live_add_conflict", directory, instructions: "Generate conflict resolution strategy in the specified directory." }, null, 2) }] };
  });
}
